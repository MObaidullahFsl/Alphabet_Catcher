clearprev:

push bp
mov bp,sp
pusha 

mov cx, [bp+6]    ; Load starting X position into CX
mov dx, [bp+4]    ; Load starting Y position into DX

dec dx            ; Move one row above the current pixel

mov si, 16        ; Outer loop counter for rows
mov bx, 16        ; Inner loop counter for columns

clear_row:
    push si       ; Save row counter
    push dx       ; Save Y position for the row
    mov si, 16    ; Reset SI for column count

clear_pixel:
    mov al, 0 ; Color (pink for debugging)
    mov ah, 0Ch   ; Write pixel in graphics mode
    int 10h       ; Call BIOS interrupt
    
    inc cx        ; Move to the next column
    dec si        ; Decrement column counter
    cmp si, 0     
    jne clear_pixel

    pop dx        ; Restore Y position
    pop si        ; Restore row counter
    inc dx        ; Move to the next row (down)
    mov cx, [bp+6]; Reset X position to the start
    dec si        ; Decrement row counter
    cmp si, 0
    jne clear_row

popa 
pop bp
ret 4



